function useT(lang) {
  return React.useCallback((k) => STRINGS[lang][k] ?? k, [lang]);
}

// Format currency — numbers always LTR
function fmtEGP(n, lang) {
  const v = Number(n || 0).toLocaleString('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 });
  return v;
}

function fmtInt(n) {
  return Number(n || 0).toLocaleString('en-US');
}

Object.assign(window, { STRINGS, useT, fmtEGP, fmtInt });
